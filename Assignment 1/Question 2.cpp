// Take length and breadth as input (both integers) and calculate the area of the rectangle.

#include <iostream>
using namespace std;

int main() 
{
    double length, breadth;
    cout<<"Enter the length: ";
    cin>>length;
    cout<<endl;
    cout<<"Enter the breadth: ";
    cin>>breadth;
    cout<<endl;
    cout<<"Area is: "<<length * breadth;
    
    return 0;
    
}
